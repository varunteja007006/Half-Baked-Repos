//function statement (or) function declaration
function sampleFunc() {
  console.log("This is a function statement");
}

//function expression
let sample_func = function () {
  console.log("This is a function expression");
};

// Anonymous function
let anonymousFunc = function () {
  console.log("This is a Anonymous function");
};

// Named function
let namedFunc = function xyz() {
  console.log("This is a named function");
};

// First class functions
// Some of the properties of first-class functions in JavaScript:
// They can be assigned to variables.
// They can be passed as arguments to other functions.
// They can be returned from other functions.
// They can be stored in objects.
// They can be used as callback functions.
